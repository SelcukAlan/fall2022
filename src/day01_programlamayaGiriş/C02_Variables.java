package day01_programlamayaGiriş;

public class C02_Variables {
    public static void main(String[] args) {

        int sayı=25;

        System.out.println(sayı);// 25

        System.out.println("sayı");// sayı

        sayı=30;
        System.out.println(sayı);//30

        sayı=sayı+20;
        System.out.println(sayı);//50

        sayı=sayı/5;
        System.out.println(sayı);//10

        sayı=sayı*20;
        System.out.println(sayı);//200

        sayı=sayı*4;
        System.out.println("sayı:"+sayı);//800
        System.out.println("sayı=sayı*4:"+sayı);//sayı=sayı*4:800







    }
}
